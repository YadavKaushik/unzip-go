const LANG_INFO: Record<Lang, { name: string; flag: string }> = {
  en: { name: 'English', flag: '🇺🇸' },
  hi: { name: 'हिन्दी', flag: '🇮🇳' },
  ta: { name: 'தமிழ்', flag: '🇮🇳' },
  te: { name: 'తెలుగు', flag: '🇮🇳' },
};

interface I18nContextType {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string) => string;
  languages: typeof LANG_INFO;
}

const I18nContext = createContext<I18nContextType>({
  lang: 'en', setLang: () => {}, t: (k) => k, languages: LANG_INFO,
});

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => (localStorage.getItem('app_lang') as Lang) || 'en');

  const setLang = (l: Lang) => { setLangState(l); localStorage.setItem('app_lang', l); };
  const t = (key: string): string => translations[lang]?.[key] || translations['en']?.[key] || key;

  return (
    <I18nContext.Provider value={{ lang, setLang, t, languages: LANG_INFO }}>
      {children}
    </I18nContext.Provider>
  );
}

export const useI18n = () => useContext(I18nContext);
export type { Lang };
